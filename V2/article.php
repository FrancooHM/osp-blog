								<h4 class="post-title">Why Sociology May Always be the Field of 20 Years Ago</h4>
								<h6 class="post-subtitle"> Subtitle </h6>
								<div class="post-content">
									<p>Kristin Thomson has posted <a href="https://medium.com/@kristinthomson/a-brief-analysis-of-class-assignments-814c6b3bca99#.mv8igzixo">an excellent piece</a> on Medium that unpacks some of the major demographic features of the top 500 sociology texts in the syllabus corpus–particularly in regard to gender, age, and publication dates.&nbsp; To hit a couple of the highlights:</p>
									<p>Among the works on the list published since 1970, only 24% are by women authors:<img class="alignleft wp-image-1362" src="http://opensyllabusproject.org/wp-content/uploads/2016/05/Screen-Shot-2016-05-18-at-12.07.20-PM-1024x376.png" alt="Screen Shot 2016-05-18 at 12.07.20 PM" width="779" height="286" srcset="http://opensyllabusproject.org/wp-content/uploads/2016/05/Screen-Shot-2016-05-18-at-12.07.20-PM-1024x376.png 1024w, http://opensyllabusproject.org/wp-content/uploads/2016/05/Screen-Shot-2016-05-18-at-12.07.20-PM-300x110.png 300w, http://opensyllabusproject.org/wp-content/uploads/2016/05/Screen-Shot-2016-05-18-at-12.07.20-PM-768x282.png 768w" sizes="(max-width: 779px) 100vw, 779px"/>
									</p>
									<p>
										The periodization of the list also yields interesting results.&nbsp; Sociology is predominantly a field built around 20-30 year old texts–a lag that possibly reflects some combination of generational seniority in the field (everyone teaches the major early works by senior figures) and perhaps a relevance horizon beyond which works no longer seem to speak to current issues.</p>
									<p>
										<img class="wp-image-1363 alignnone" src="http://opensyllabusproject.org/wp-content/uploads/2016/05/Screen-Shot-2016-05-18-at-12.16.04-PM-1024x895.png" alt="Screen Shot 2016-05-18 at 12.16.04 PM" width="779" height="680" srcset="http://opensyllabusproject.org/wp-content/uploads/2016/05/Screen-Shot-2016-05-18-at-12.16.04-PM-1024x895.png 1024w, http://opensyllabusproject.org/wp-content/uploads/2016/05/Screen-Shot-2016-05-18-at-12.16.04-PM-300x262.png 300w, http://opensyllabusproject.org/wp-content/uploads/2016/05/Screen-Shot-2016-05-18-at-12.16.04-PM-768x671.png 768w" sizes="(max-width: 779px) 100vw, 779px"/>
									</p>
									<p>This really whets my appetite for the day when we can do this natively in the Syllabus Explorer, across any combination of fields and texts.&nbsp; The whole piece is worth a look.</p>								
