									</h4>
								</header>
								<!-- .entry-header -->
								<div class="entry-content">
									<p>
									</p>
									<p>This month’s honorees are Carl Bergstrom and Jevin West from the University of Washington for their very ambitious INFO 198 / BIOL 106B course:</p>
									<blockquote>
										<h4>Calling Bullshit in the Age of Big Data</h4>
										<p>Our learning objectives are straightforward. After taking the course, you should be able to:</p>
										<ul>
											<li>Remain vigilant for bullshit contaminating your information diet.</li>
											<li>Recognize said bullshit whenever and wherever you encounter it.</li>
											<li>Figure out for yourself precisely <i>why</i> a particular bit of bullshit is bullshit.</li>
											<li>Provide a statistician or fellow scientist with a technical explanation of why a claim is bullshit.</li>
											<li>Provide your crystals-and-<a href="https://www.nhmrc.gov.au/_files_nhmrc/publications/attachments/cam02_nhmrc_statement_homeopathy.pdf">homeopathy</a> aunt or casually racist uncle with an accessible and persuasive explanation of why a claim is bullshit.</li>
										</ul>
										<p>We will be astonished if these skills do not turn out to be among the most useful and most broadly applicable of those that you acquire during the course of your college education.</p>
									</blockquote>
									<p>
										<a href="http://callingbullshit.org/syllabus.html">More</a>.</p>
