							<article id="post-1409" class="post-1409 post type-post status-publish format-standard hentry category-news">
								<header class="entry-header">
									<h4 class="entry-title">
										<a href="http://opensyllabusproject.org/job-opportunity-at-the-open-syllabus-project-web-applications-developer-javascript-and-python/" rel="bookmark">Job opportunity at the Open Syllabus Project: Web Applications Developer (Javascript and Python)</a>
									</h4>
								</header>
								<!-- .entry-header -->
								<div class="entry-content">
									<p>
									</p>
									<p>The <a href="http://explorer.opensyllabusproject.org/">Open Syllabus Project</a> is an academic data mining project based at Columbia University that’s analyzing a corpus of 1M+ college course syllabi. We’re building a wide range of tools and services for audiences both inside and outside the university. We aim to:</p>
									<ul>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Help instructors discover new resources and design courses.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Help university administrators understand the structure of the offerings at their institutions, and to help librarians allocate resources for books, databases, and services.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Develop &nbsp;new scholarly metrics that capture which resources are being used in the classroom.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Build book recommendation services for lifelong learning</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Analyze trends in the sequencing of books and concepts on syllabi, to build a “dependency graph” of knowledge.</span>
										</li>
									</ul>
									<p>
										<span style="font-weight: 400;">We launched a beta version of the project with an op-ed in the New York Times last year. &nbsp;Since then the project has appeared in Nature, Time, The Washington Post, The Chronicle of Higher Education, MarketWatch, Der Spiegel, Business Insider, Lifehacker, FiveThirtyEight, WNYC, QZ, and elsewhere. It’s also been picked up by major news outlets in Europe, Russia, China, Japan, South Korea, Ukraine, Egypt, and Mexico. With new funding from the Sloan, Hewlett, and Templeton foundations, we’re working towards second release of the project that will feature much larger collections of syllabi, books, authors, institutions, and publishers. </span>
									</p>
									<p>
										<span style="font-weight: 400;">We’re hiring a full-stack web applications developer to take a leading role in the development of these public-facing web services. We’re looking for a developer who has significant experience at both layers of the web stack – someone who enjoys building large, stateful Javascript applications, and also is able to build and maintain the server-side APIs that feed these client applications.</span>
									</p>
									<p>
										<b>Responsibilities</b>
									</p>
									<ul>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Build a API in Python (Flask or Django) that organizes the results of the metadata extraction pipeline into web-facing data stores (Elasticsearch, Postgres) and exposes well-structured REST endpoints for the client application.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">In collaboration with designers and contract developers, build a front-end application using React and Redux / MobX that surfaces the data on the web.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Work with the data engineering team to define data requirements for the front end application.</span>
										</li>
									</ul>
									<p>
										<b>Requirements</b>
									</p>
									<ul>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">3+ years of professional experience in software engineering.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Demonstrated ability to build high-quality, performant, maintainable web applications that serve sizable traffic.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Experience building large, stateful Javascript applications with React and React-ecosystem libraries like Redux and MobX.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Experience with modern Javascript build tools like Webpack or Gulp.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Experience with server-side Python development with Flask or Django.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Commitment to sustainable engineering practices – automated testing and deployment, continuous integration, and reproducible development environments.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">An eye for clean, readable, extensible, well-tested code.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Experience with remote / distributed collaboration on GitHub.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Ability to work in a team environment, but also the initiative to take on difficult, open-ended problems and see them to completion.</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Committed to building a tolerant, respectful, and safe work environment.</span>
										</li>
									</ul>
									<p>
										<b>Nice to have</b>
									</p>
									<ul>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Experience with Elasticsearch</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Experience with infrastructure automation tools like Ansible</span>
										</li>
										<li style="font-weight: 400;">
											<span style="font-weight: 400;">Data visualization with d3</span>
										</li>
									</ul>
									<p>
										<span style="font-weight: 400;">First and foremost – we’d love to find a collaborator who can contribute new ideas and help drive the project in new directions. OSP is strongly committed to building a diverse engineering team.</span>
									</p>
									<p>
										<b>Salary</b>
										<span style="font-weight: 400;">: Competitive</span>
									</p>
									<p>
										<b>Commitment</b>
										<span style="font-weight: 400;">: Full-time for 2 years, renewable. </span>
									</p>
									<p>
										<b>Location</b>
										<span style="font-weight: 400;">: Remote or NYC</span>
										<br>
											<span style="font-weight: 400;">If interested, please send an email and CV to <a href="mailto:syllabusopen@gmail.com">syllabusopen@gmail.com</a>
											</span>
										</p>
