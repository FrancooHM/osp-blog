										<h2 class="entry-title">
											<a href="http://opensyllabusproject.org/syllabus-of-the-month-february/" rel="bookmark">Syllabus of the Month (February)</a>
										</h2>
									</header>
									<!-- .entry-header -->
									<div class="entry-content">
										<p>
										</p>
										<p>This month we highlight&nbsp;RISD&nbsp;Professor Clement Valla’s <a href="http://udf13.cvalla.com/syllabus/">course,&nbsp;<em>Uncreative Design</em>
											</a>,&nbsp;and ponder its relationship to the OSP.</p>
										<blockquote>
											<h2>DESCRIPTION</h2>
											<p>“In 1969 the conceptual artist Douglas Heubler wrote, ‘The world is full of objects, more or less interesting; I do not wish to add any more.’” So opens Kenneth Goldsmith’s <em>Uncreative Writing</em>, published in 2011. Following Goldsmith’s lead, this class will explore various strategies in art, writing and political activism that will lead us to an uncreative design. We will make use of copying, repetition, appropriation, detournement and bricolage in a series of studio experiments. Though the class will be focused on ways of (not)making, class participation and discussions of assigned readings will also play a major role in guiding studio work, and in evaluating student projects. There are no prerequisites, though students should be willing to take major risks and have a very open approach to different modes of working.</p>
											<p>Creativity, intuition, improvisation, composed, hand-made, unique, original, subjective, genius, authored. These are all to be avoided in this class. Rather what we create will be uncreative, systematic, scripted, chance based, calculated, mass produced, digital, appropriated, objective and copied. The role of contemporary producers is no longer be to create new things, but to channel, frame, re-assemble and contextualize existing things – from creative production towards an ‘uncreative’ production. Uncreative Design explores how new meaning is produced by collecting, archiving, captioning, erasing, parsing. There are many examples of this work and theory in other disciplines, including writing, art, new media, music, film and video.</p>
										</blockquote>
									</div>
